package com.example.pencatatantransaksi.Model;

public class ModelSatuan {
    private String id_satuan, nama_satuan, jumlah;

    public ModelSatuan() {

    }

    public String getId_satuan() {
        return id_satuan;
    }

    public void setId_satuan(String id_satuan) {
        this.id_satuan = id_satuan;
    }

    public String getNama_satuan() {
        return nama_satuan;
    }

    public void setNama_satuan(String nama_satuan) {
        this.nama_satuan = nama_satuan;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }
}
